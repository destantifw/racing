    }
    
    public static var grayPampas: Color{
        return Color(UIColor(red: 246/255, green: 244/255, blue: 241/255, alpha: 1))
    }
    
    public static var grayText: Color{
        return Color(UIColor(red: 119/255, green: 119/255, blue: 123/255, alpha: 1))
    }
}

